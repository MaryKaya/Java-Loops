package July8;

public class TriangleWithNumbers {
    public static void main(String[] args) {
        int number = 0;
        for (int i = 1; i < 6; i++) {


            for (int j = 1; j <= i; j++) {


                number = number + 1;
                System.out.print(number );

            }

            System.out.println();

        }


    }


}





